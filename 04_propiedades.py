import turtle
t = turtle.Turtle()
t.speed(5) # 1:slowest, 3:slow, 5:normal, 10:fast, 0:fastest
t.setpos(0,0)
t.pencolor(255,0,0)
t.pensize(5)
t.circle(30)
t.penup()
t.setpos(60,60)
t.pendown()
t.pencolor(0,255,0)
t.pensize(10)
t.circle(30)
t.penup()
t.setpos(120,120)
t.pendown()
t.pencolor(0,0,255)
t.pensize(15)
t.circle(30)
